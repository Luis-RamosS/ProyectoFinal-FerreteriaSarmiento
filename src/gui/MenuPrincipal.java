    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        int fila = tablaClientes.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente de la tabla para actualizar");
        } else {
            Cliente cl = new Cliente();
            ClienteDao dao = new ClienteDao();

            // 1. Capturamos el ID de la fila seleccionada (columna 0)
            int id = Integer.parseInt(tablaClientes.getValueAt(fila, 0).toString());

            // 2. Llenamos el objeto con los datos de los JTextFields
            cl.setId_cliente(id);
            cl.setIdentidad(txtIdentidad.getText());
            cl.setNombre_completo(txtNombre.getText());
            cl.setCorreo(txtCorreo.getText());
            cl.setTelefono(txtTelefono.getText());
            cl.setDireccion(txtDireccion.getText());

            // 3. Ejecutamos la actualización
            if (dao.actualizar(cl)) {
                JOptionPane.showMessageDialog(this, "Cliente actualizado con éxito");
                listarClientes(); // Para que los cambios se vean en la tabla
                limpiar();  // (Opcional) Para vaciar los textos
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar");
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

