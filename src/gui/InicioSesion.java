    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarSesionActionPerformed
        // TODO add your handling code here:
        // 1. Obtener los datos de los campos
        String nombre = txtUsuario.getText();
        String pass = new String(txtContraseña.getPassword());

        // 2. Validar que no estén vacíos
        if (!nombre.equals("") && !pass.equals("")) {
            UsuarioDao dao = new UsuarioDao();
            Usuario us = dao.login(nombre, pass);

            // 3. Verificar si el usuario existe en la BD
            if (us.getNombre_usuario() != null) {
                JOptionPane.showMessageDialog(this, "Bienvenido " + us.getNombre_usuario());

                // 4. Abrir el Menú Principal y cerrar el Login
                MenuPrincipal menu = new MenuPrincipal();
                menu.setVisible(true);
                this.dispose(); // Cierra la ventana actual
            } else {
                JOptionPane.showMessageDialog(this, "Acceso denegado: Usuario o contraseña incorrectos");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos");
        }
    }//GEN-LAST:event_btnIniciarSesionActionPerformed

